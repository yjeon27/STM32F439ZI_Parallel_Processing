	  HAL_NVIC_DisableIRQ(I2Cx_DMA_TX_IRQn);
	  HAL_NVIC_DisableIRQ(I2Cx_DMA_RX_IRQn);

	  /*##-5- Disable the NVIC for I2C ###########################################*/
